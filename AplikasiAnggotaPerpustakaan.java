    public void table() {
        KoneksiPerpustakaan konek = new KoneksiPerpustakaan();
        konek.koneksi();
        try {

            String sql = "select * from `daftaranggota`";
            pst = konek.con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            tabelanggota.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    public void selectByAbsen(int absen) {
        KoneksiPerpustakaan konek = new KoneksiPerpustakaan();
        konek.koneksi();

        try {
            String sql = "select * from daftaranggota where absen like '%" + absen + "%'";
            pst = konek.con.serverPrepareStatement(sql);
            rs = pst.executeQuery(sql);
            tabelanggota.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

