    private void tabelanggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelanggotaMouseClicked

        DefaultTableModel dtm = (DefaultTableModel) tabelanggota.getModel(); //create a model
        int selectedRowIndex = tabelanggota.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        KoneksiPerpustakaan konek = new KoneksiPerpustakaan();
        konek.koneksi();

        try {

            String sql = "select * from daftaranggota where absen like '%" + id + "%'";
            pst = konek.con.serverPrepareStatement(sql);

            ResultSet rs = rs = pst.executeQuery(sql);
            while (rs.next()) {
                 in_absen.setText(rs.getString("absen"));
                in_noid.setText(rs.getString("nomerid"));
                in_namaid.setText(rs.getString("namaid"));
                in_kelas.setText(rs.getString("kelas"));
                in_tanggal.setDateFormatString(rs.getString("tanggalbergabung"));

            }
        } catch (SQLException ex) {
        }
        //        TODO add your handling code here

    }//GEN-LAST:event_tabelanggotaMouseClicked

